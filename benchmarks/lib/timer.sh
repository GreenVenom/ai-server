#!/bin/bash

############################################################
#
# Personal AI Platform
#
# Benchmark Framework
#
# Script: timer.sh
#
# Purpose:
# High resolution timing utilities.
#
# Version: 1.0.0
#
############################################################

[[ -n "${BENCHMARK_TIMER_LOADED:-}" ]] && return
BENCHMARK_TIMER_LOADED=1

############################################################
# Timer State
############################################################

TIMER_START_NS=0

TIMER_END_NS=0

############################################################
# Start
############################################################

timer_start() {

    TIMER_START_NS=$(date +%s%N)

}

############################################################
# Stop
############################################################

timer_stop() {

    TIMER_END_NS=$(date +%s%N)

}

############################################################
# Nanoseconds
############################################################

timer_elapsed_ns() {

    echo $((TIMER_END_NS - TIMER_START_NS))

}

############################################################
# Milliseconds
############################################################

timer_elapsed_ms() {

    echo $(((TIMER_END_NS - TIMER_START_NS)/1000000))

}

############################################################
# Seconds
############################################################

timer_elapsed_seconds() {

    awk "BEGIN {printf \"%.3f\", (${TIMER_END_NS}-${TIMER_START_NS})/1000000000}"

}

############################################################
# Generic Measurement
############################################################

measure() {

    timer_start

    "$@"

    timer_stop

}

############################################################
# Timestamp
############################################################

current_timestamp() {

    date +"%Y-%m-%d %H:%M:%S"

}

############################################################
# ISO Timestamp
############################################################

iso_timestamp() {

    date -u +"%Y-%m-%dT%H:%M:%SZ"

}